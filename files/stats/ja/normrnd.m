% NORMRND   正規分布からの乱数の配列
%
% R = NORMRND(MU,SIGMA) は、平均 MU と標準偏差 SIGMA をもつ正規分布から
% 抽出された乱数の配列を出力します。R の大きさは、MU と SIGMA が共に
% 配列の場合、それらに共通する大きさになります。どちらかのパラメータが
% スカラの場合、R の大きさは、もう一方のパラメータと同じ大きさになります。
%
% R = NORMRND(MU,SIGMA,M,N,...) または R = NORMRND(MU,SIGMA,[M,N,...]) は、
% M×N×... の配列を出力します。
%
%   参考 NORMCDF, NORMFIT, NORMINV, NORMLIKE, NORMPDF, NORMSTAT,
%   RANDOM, RANDN.


