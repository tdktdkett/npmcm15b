%   pdf の X における値を返します。MUと SIGMA は、それぞれ関連の正規分布の
%   平均と標準偏差です。Y のサイズは、入力引数と同じサイズです。スカラの
%   入力は、もう一方の入力と同じサイズの定数行列として機能します。
% 
%   MU と SIGMA のデフォルト値は、それぞれ、0 と 1 です。
